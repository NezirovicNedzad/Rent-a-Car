    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedIndex = klijentComboBox.getSelectedIndex();
        if (selectedIndex == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Molimo izaberite klijenta.");
            return;
        }

        try {
            // 1. Parsiranje datuma iz tvog formata (M/d/yy)
            // DateTimeFormatter.ofPattern("M/d/yy") odgovara tvom formatu 6/26/26
            java.time.format.DateTimeFormatter dtf = java.time.format.DateTimeFormatter.ofPattern("M/d/yy");

            java.time.LocalDate danas = java.time.LocalDate.now();
            java.time.LocalDate vracanje = java.time.LocalDate.parse(datumVracanja.getText(), dtf);

            // 2. Kalkulacija broja dana i cene
            long brojDana = java.time.temporal.ChronoUnit.DAYS.between(danas, vracanje);
            if (brojDana <= 0) brojDana = 1; // Ako je isti dan ili greska, racunamo minimum 1 dan

            double cenaPoDanu = Double.parseDouble(cenaTxt.getText());
            double ukupnaCena = brojDana * cenaPoDanu;

            // 3. SQL Upiti
            String insertUgovor = "INSERT INTO ugovori (DatumSklapanja, PlaniraniDatumVracanja, TroskoviNajma, NacinPlacanja, Status, AutomobilID, KlijentID,SluzbenikID) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
            String updateAuto = "UPDATE automobili SET Status = 'Izdat' WHERE Id = ?";

            try (java.sql.Connection conn = db.DBConnection.getConnection()) {
                conn.setAutoCommit(false); // Transakcija

                // Insert ugovora
                try (java.sql.PreparedStatement pstUgovor = conn.prepareStatement(insertUgovor)) {
                    pstUgovor.setDate(1, java.sql.Date.valueOf(danas));
                    pstUgovor.setDate(2, java.sql.Date.valueOf(vracanje)); // Sada ce biti ispravno 2026-06-28
                    pstUgovor.setDouble(3, ukupnaCena);
                    pstUgovor.setString(4, jComboBox1.getSelectedItem().toString());
                    pstUgovor.setString(5, "Aktivan");
                    pstUgovor.setInt(6, SelectedCar.Id);
                    pstUgovor.setInt(7, klijentIds.get(selectedIndex));
                    pstUgovor.setInt(8,Session.id);
                    pstUgovor.executeUpdate();
                }

                // Update statusa automobila
                try (java.sql.PreparedStatement pstAuto = conn.prepareStatement(updateAuto)) {
                    pstAuto.setInt(1, SelectedCar.Id);
                    pstAuto.executeUpdate();
                }

                conn.commit();
                javax.swing.JOptionPane.showMessageDialog(this, "Ugovor kreiran! Ukupna cena za " + brojDana + " dana je: " + ukupnaCena);
                
                ContractsPage contracts = new ContractsPage();
                contracts.setVisible(true);

                this.dispose(); 
            }
        } catch (java.time.format.DateTimeParseException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pogrešan format datuma! Unesite format kao na primeru: 6/26/26");
        } catch (HeadlessException | NumberFormatException | SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Greška: " + e.getMessage());
        }  
    }//GEN-LAST:event_jButton2ActionPerformed

