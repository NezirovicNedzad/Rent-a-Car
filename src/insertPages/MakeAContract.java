    private void proveriIzracunaj() {
        String datumStr = datumVracanja.getText();
        if (datumStr.isEmpty() || klijentComboBox.getSelectedIndex() == -1) {
            jButton2.setEnabled(false);
            return;
        }

        try {
            java.time.format.DateTimeFormatter dtf = java.time.format.DateTimeFormatter.ofPattern("M/d/yy");
            java.time.LocalDate danas = java.time.LocalDate.now();
            java.time.LocalDate vracanje = java.time.LocalDate.parse(datumStr, dtf);

            if (vracanje.isAfter(danas)) {
                long brojDana = java.time.temporal.ChronoUnit.DAYS.between(danas, vracanje);
                double cenaPoDanu = Double.parseDouble(cenaTxt.getText());
                double ukupna = brojDana * cenaPoDanu;

                ukupnaCenaTxt.setText(String.format("%.2f", ukupna));
                jButton2.setEnabled(true);
            } else {
                jButton2.setEnabled(false);
            }
        } catch (Exception e) {
            jButton2.setEnabled(false); // Ako format datuma nije dobar
        }
    }

    private void loadClients() {
        // 1. Postavi prazan model - ovo sigurno briše sve prethodne stavke
        klijentComboBox.setModel(new javax.swing.DefaultComboBoxModel<>());

        klijentIds.clear(); 

        String query = "SELECT Id, ime, prezime FROM klijenti"; 

        try (java.sql.Connection conn = db.DBConnection.getConnection();
             java.sql.PreparedStatement pst = conn.prepareStatement(query);
             java.sql.ResultSet rs = pst.executeQuery()) {

            while (rs.next()) {
                String klijent = rs.getString("ime") + " " + rs.getString("prezime");
                // 2. Dodaj stavku
                klijentComboBox.addItem(klijent);
                klijentIds.add(rs.getInt("Id"));
            }

            // Opcionalno: proveri da li je lista prazna
            if (klijentIds.isEmpty()) {
                System.out.println("Upozorenje: Nema klijenata u bazi!");
            }

        } catch (java.sql.SQLException e) {
            logger.log(java.util.logging.Level.SEVERE, "Greška pri učitavanju klijenata", e);
        }
    }
